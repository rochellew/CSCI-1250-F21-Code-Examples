// This class demonstrates the increment ( ++ ) and
// decrement ( -- ) operators

public class IncrementDecrement
{
    public static void main(String[] args) 
    {
        int number = 4; // number start with a value of 4

        // Display the value in number
        System.out.println("Number is " + number);
        System.out.println("I will now increment number");

        // Increment number (add 1)
        number++;

        // Display the value in number
        System.out.println("Number is now " + number);
        System.out.println("I will now decrement number");

        // Decrement number (minus one)
        number--;

        // Display the value in number
        System.out.println("Number is " + number);
    }
}